public class pmmd 
{
	public static void main(String non[]) 
	{
		int n=Integer.parseInt(non[0]);
		int o=Integer.parseInt(non[1]);
		int nn=Integer.parseInt(non[2]);

			System.out.println( "Plus="+(n+o+nn));
			System.out.println( "Minus="+(n-o-nn));
			System.out.println( "Multiply="+(n*o*nn));
			System.out.println( "Divide="+(n/o/nn));
	}
}